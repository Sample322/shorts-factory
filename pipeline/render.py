def _wait_for_music_choice(
    decision_file: str | None,
    variants: list,
    timeout_sec: int = 1200,
) -> Path | None:
    """Поллит decision.json пока пользователь не выберет вариант или скип.

    decision.json формат: {"music_index": 1|2|3} или {"music_index": null} для пропуска.
    """
    if not decision_file:
        # decision-файл не настроен — просто берём первый вариант автоматом
        return variants[0].path if variants else None

    df = Path(decision_file)
    deadline = time.monotonic() + timeout_sec
    while time.monotonic() < deadline:
        if df.exists():
            try:
                data = json.loads(df.read_text(encoding="utf-8"))
                idx = data.get("music_index")
                if idx is None:
                    return None  # пользователь нажал "пропустить музыку"
                for v in variants:
                    if v.index == idx:
                        return v.path
                log.warning(f"music_index={idx} не нашёлся в вариантах")
                return None
            except (json.JSONDecodeError, OSError):
                pass
        time.sleep(1.0)
    log.warning(f"Таймаут {timeout_sec}с ожидания выбора музыки")
    return None


